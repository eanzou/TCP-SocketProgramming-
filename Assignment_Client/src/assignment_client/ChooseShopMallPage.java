    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
        // TODO add your handling code here:
        try 
        {
            oos.writeObject("next");
            
            selected_item[0] = (String) shop.getSelectedItem(); //record
            selected_item[1] = (String) loc.getSelectedItem();
            
            for (int i=0; i<2; i++)
                oos.writeObject(selected_item[i]); //pass all items selected
            
            if (pass_choose == 2)
            {
                page = "ShopFood";
                oos.writeObject(page); //pass page number
                ChooseFoodPage choosefood_page = new ChooseFoodPage(socket, oos, ois, pass_choose, selected_item, takeonce);
                choosefood_page.setLocation(this.getX(), this.getY());
                choosefood_page.setVisible(true); 
                this.setVisible(false);
            }
            else
            {
                page = "ShopItem";
                oos.writeObject(page); //pass page number
                ShopItemPage shopitem_page = new ShopItemPage(socket, oos, ois, pass_choose, selected_item, takeonce);
                shopitem_page.setLocation(this.getX(), this.getY());
                shopitem_page.setVisible(true); 
                this.setVisible(false);
            }

        } catch (IOException ex) {} catch (ClassNotFoundException ex) {start.fail_to_connect();}
    }//GEN-LAST:event_nextActionPerformed

