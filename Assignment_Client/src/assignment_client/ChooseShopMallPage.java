    public void change_combo()
    {
        String current;
        current = (String) loc.getSelectedItem();
        String cat1_loc = "Xiamen University Malaysia";
        String cat1_loc2 = "The Olive";

        if (current.equals(cat1_loc) || current.equals(cat1_loc2))
        {
            shop.removeAllItems();
            for (int i=0; i<3; i++)
                shop.addItem(cat1[i]);
        }
        else 
        {
            shop.removeAllItems();
            for (int i=0; i<3; i++)
                shop.addItem(cat2[i]);
        }    
        
    }

